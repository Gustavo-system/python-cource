# Operadores de comparacion

"""
Nos permiten saber si una variable es la condicion que se le pase como operador, en la mayoria de veces esto 
nos retorna un valor booleano (bool)

menor que       <
mayor que       >
menor igual     <=
mayor igual     >=
igual           ==
distintos       !=

"""

# comparamos si el primer valor es igual al segundo valor
mi_comparacion = 200 == 200

# comparamos si el primer valor es mayor al segundo valor
mi_comparacion = 200 > 200

# comparamos si el primer valor es menor al segundo valor
mi_comparacion = 200 < 200

# comparamos si el primer valor es mayo o igual al segundo valor
mi_comparacion = 200 >= 200

# comparamos si el primer valor es menor o igual al segundo valor
mi_comparacion = 200 <= 200

# comparamos si el primer valor es disitinto al segundo valor
mi_comparacion = 200 != 200

print(mi_comparacion)